      <p class="shengtai_subTitle" style="margin-left: 10%"><span style="color: #3e99cb">>>>班夫国家公园<<<</span><br>
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;-----大千世界中的童话王国<br><br></p>
 
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;有人说，班夫是落基山脉的灵魂。因为，在班夫你可以欣赏到一年四季风景不同的落基山脉，每一处景色都可以成为你手机的壁纸深深地吸引你的注意。<br><br>
 
第一站：班夫镇。<br>
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;从卡尔加里自驾约130km后你将来到童话小镇班夫镇。这时你要做的第一件事最好就是前往硫磺山乘坐班夫空中缆车。相信我，站在2281米高的硫磺山巅，你将看到最美的班夫小镇。
    </div>

    <img src="/images/shengtai/national/2.png" width="100%" style="height:18vw">

    <div class="shengtai_content" style="width: 90%;margin:0 auto;background: rgba(255,255,255,0.5);padding: 2vw 0;font-weight: bold">
      第二站：路易斯湖、梦莲湖              <br><br>
 
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;翡翠的丝绸般有"一年四季的魔鬼"之称的路易斯湖铺在山谷间，原始森林里生长着数百万株长青树，顶峰积雪的群山绵延起伏一直伸向远方，湖水随光线深浅由蓝变绿，满湖碧透。而冬天的路易斯湖更是有着她无可替代的美。<br><br>
 
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;与路易斯湖并称为班夫双壁的是距离其不远的梦莲湖。梦莲湖湖底积满了富含矿物质的碎石，经年累月的沉积形成了变幻多姿的碧蓝色湖水当你伫立在湖边，遥望远处连绵的落基山脉，我相信你会不禁感叹大自然造物的神奇。
    </div>

    <img src="/images/shengtai/national/3.png" width="100%" style="height:18vw">

    <div class="shengtai_content" style="width: 90%;margin:0 auto;background: rgba(255,255,255,0.5);padding: 2vw 0;font-weight: bold">
      &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;如果你是夏季来到班夫，那就一定要乘坐独木舟，在蓝色的湖面上泛舟前行，触摸远处雪山上溶雪汇成的无比冰凉无比透彻的湖水。<br>
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;如果你是冬季来到班夫，那么我推荐你一定要去感受一下班夫的冰上垂钓。即便你是新手，你也可以收获颇丰。
    </div>

