    <div class="shengtai_content" style="width: 90%;margin:0 auto;background: rgba(255,255,255,0.5);padding: 2vw 0;font-weight: bold">
      <p class="shengtai_subTitle" style="margin-left: 10%"><span style="color: #4dba25">>>>幽鹤国家公园<<<</span><br>
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;------落基山脉中的秘境<br><br></p>
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;幽鹤国家公园的英文叫做“Yoho”,这名字可不是乱取的，当你看见她的第一眼，看见她那色如翡翠的湖泊、世外桃源般的景色，我想你也会发出这么一声感叹： “YO-HO！”<br><br>
​
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;欧哈拉湖还有个更加响亮的名头，他是世界上最美的高山徒步路线之一，是世界级的徒步者的天堂。也正是因为他的名声在外，导致现在欧哈拉湖对生态环境的保护已经近乎“变态”，它设置了各种准入障碍：通往欧哈拉湖腹地的那条公路，不许任何私家车进入，也不许使用自行车代步，只有一辆大巴车可从停车场前往，不过预订车票比中奖还难，需要提前三个月申请。<br>
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;这里的高地徒步路线沿途要经过七个大大小小的湖泊。一路上要穿过高山峡谷，松林草地，湖泊溪流。无论是翠绿的夏日还是金色的秋天，这里都是喜欢徒步的人们的最爱。
    </div>

    <img src="/images/shengtai/national/5.png" width="100%" style="height:18vw">

    <div class="shengtai_content" style="width: 90%;margin:0 auto;background: rgba(255,255,255,0.5);padding: 2vw 0;font-weight: bold">
       <p class="shengtai_subTitle" style="margin-left: 10%"><span style="color: #0bb092">>>>冰原大道<<<</span><br>
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;--------绝美的93号公路<br><br></p>
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;冰原大道，又称“北美93号公路”，它被公认为世界上风景最美丽的公路之一。它贯穿南北， 是贾斯珀国家公园和班夫国家公园之间的“大陆脊梁”。特别是如果你能驾驶敞篷车行走于此，你会真切感受到自驾所带给你的无穷的乐趣。试想头顶蓝天、白云，和煦的阳光轻轻地洒在脸庞上，你会有种说不出的温暖，因为这里有太多的地方值得你细细品味，走走停停。不过你要注意，有时候你可能会遇上“堵车”，但我觉得这是你的幸运，因为你遇上的是‘堵熊’、‘堵鹿’，这是多么难能可贵的“堵车”体验啊，记得要做一名礼貌的司机，让“行人”先走哦。
    </div>

    <img src="/images/shengtai/national/6.png" width="100%" style="height:18vw">

