在这里，你也可以漫步在冰川天空步道，沿着完全开放的峭壁步道抵达辛华达峡谷上方280米的玻璃地板观景台。这是如此的惊险与刺激。
    </div>

    <img src="/images/shengtai/national/7.png" width="100%" style="height:18vw">

    <div class="shengtai_content" style="width: 90%;margin:0 auto;background: rgba(255,255,255,0.5);padding: 2vw 0;font-weight: bold">
       <p class="shengtai_subTitle" style="margin-left: 10%"><span style="color: #080357">>>>贾斯珀国家公园<<<</span><br>
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;------落基山脉的心脏<br><br></p>
         这里，是落基山脉的心脏，地球上有着最干净空气的地方之一；这里有世界第二大暗夜星空保护区，是最完美的观星胜地。在贾斯珀小镇上，摩托观光格外的刺激。你可以穿上皮衣皮裤，戴上皮手套、墨镜和头盔，坐在酷酷的“骑警”身后，他能带你来一次像“过山车”一样刺激的旅行。<br><br>
 
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;从贾斯珀小镇出发仅需15分钟，你会到达纯净无污染的暗夜星空下，风光迤逦的金字塔湖和金字塔山。在这里你可以头戴头灯，仰望星空，你可以观看到银河系的轴心从阿萨巴斯卡冰川的中心缓缓上升，它让你感到自己与宇宙正相互联系着。
    </div>

