      <p class="shengtai_subTitle" style="margin-left: 10%"><span style="color: #a98200">>>>卡尔加里<<<</span><br>
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;------牛仔之城的启程<br><br></p>
 
         &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;卡尔加里，一座位于加拿大艾伯塔省南部的落基山脉城市，它以终年不化的壮美雪山为城市背景。由于这里属于落基山脉的“脚山”地带，所以你从卡尔加里自驾出发，开始落基山脉之旅是最完美的选择。
