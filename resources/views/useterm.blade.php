	<h3 style="text-align:center;"><b>旅游安全须知及注意事项</b></h3>
	<div style="font-size:20px; margin-top:1.5%;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;为了增强游客安全意识，普及旅游安全基本常识，使游客参加活动的计划圆满、愉快、顺利地完成，请<span style="color:red;">认真阅读</span>本须知并<span style="color:red;">严格履行</span>，防止意外事件。</div>
	<div class="termContent">
		<p>一、<span style="color:#73BF00;">乘车（机、船）</span>安全事项<br>
		1、游客在机、车、船停稳后方可上下机、车、船。并按机场、车站、<span style="color:red;">港口安全管理规定</span>或<span style="color:red;">指示标志</span>通行及排队上下机、车、船；要讲究文明礼貌，先照顾老人、儿童、妇女；切勿拥挤，以免发生意外。请勿携带<span style="color:red;">违禁物品</span>。<br>
		2、在机、车、船临时停靠期间，服从服务人员安排，<span style="color:red;">请勿远离</span>。<br>
		3、游客在乘车途中，请不要与司机交谈和催促司机开快车，违章超速和超车行驶；不要将头、手、脚或行李物品伸出窗外，以防意外发生。<br>
		4、游客下车浏览、就餐、购物时，请注意<span style="color:red;">关好旅游车窗</span>，贵重物品请自己<span style="color:red;">随身携带</span>并妥善保管。<br>
		5、游客乘坐飞机时，应注意遵守民航乘机安全管理规定，特别是不要在飞机上使用手机等无线电通讯工具或电子游戏等，建议全程系好安全带。<br>
		</p>
	</div>
	<div class="termContent">
		<p>二、<span style="color:#73BF00;">住宿</span>安全事项<br>
		1、游客入住酒店后，应了解酒店安全须知，熟悉酒店的<span style="color:red;">紧急出口</span>、<span style="color:red;">安全楼梯</span>的位置和<span style="color:red;">安全转移的路线</span>。<br>
		2、注意检查酒店为你所配备的用品（卫生间防滑垫等）是否齐全，有无破损，如有不全或破损，<span style="color:red;">请立即向酒店服务员或导游报告</span>。如因当地条件所限，未能配备，请游客小心谨慎，防止发生意外。<br>
		3、<span style="color:red;">贵重物品</span>应存放于酒店总台保险柜或自行妥善保管，外出时不要放在房间内。<br>
		4、不要将自己住宿的<span style="color:red;">酒店、房号</span>随便告诉陌生人；不要让陌生人或自称酒店的维修人员随便进入房间；出入房间要锁好房门，睡觉前注意房门窗是否关好，<span style="color:red;">保险锁</span>是否锁上；物品最好放于身边，<span style="color:red;">不要放在靠窗的地方</span>。<br>
		5、游客入住酒店后需要外出时，应<span style="color:red;">告知随团导游</span>；在酒店总台领一张酒店房卡，卡片上有酒店地址、电话或抄写酒店地址或电话；如果您迷路时，可以按地址询问或搭乘出租车，安全顺利返回住所。<br>
		6、 遇紧急情况千万不要慌张。发生<span style="color:red;">火警</span>时<span style="color:red;">不要搭乘电梯或随意跳楼</span>；镇定地判断火情，主动地实行自救。若身上着火，可就地打滚，或用重衣物压火苗；必须穿过有浓 烟的走廊、通道时，用浸温的衣物披裹身体，捂着口鼻，贴近地、顺墙爬走；大火封门无法逃出时，<span style="color:red;">可采用浸温的衣物披裹身体被褥堵门缝或泼水降温的办法等待救 援或摇动色彩鲜艳的衣物呼唤救援人员</span>。
		</p>
	</div>
	<div class="termContent">
		<p>三、<span style="color:#73BF00;">饮食卫生</span>安全事项<br>
		1、在旅游地购买食物需注意商品质量，发现食物不卫生或有异味变质的情况，<span style="color:red;">切勿食用</span>。<br>
		2、不要接受和食用陌生人<span style="color:red;">赠送的香烟、食物和饮品</span>，防止意外发生。<br>
		3、旅游期间要合理饮食，不要暴饮、暴食或贪食。<br>
		4、为防止在旅途中水土不服，游客应自备一些<span style="color:red;">常用药品</span>以备不时之需。切勿随意服用他人所提供之药品。<br>
		5、旅行社不提倡、不安排饮酒，并对游客因饮酒发生的意外不承担责任。喜欢喝酒的游客在旅途中应严格控制自己的酒量，饮酒时最好不超过本人平时的<span style="color:red;">三分之一</span>；若出现酗酒闹事、扰乱社会秩序、侵害他人权益以及造成自身损害的一切责任由肇事者承担。
		</p>
	</div>
	<div class="termContent">
		<p>四、<span style="color:#73BF00;">游览观景</span>安全事项<br>
		1、听取<span style="color:red;">当地导游</span>有关安全的提示和忠告，主要应预防意外事故和突发性疾病的发生。<br>
		2、经过危险地段（如陡峭、狭窄的同路、潮湿泛滑的道路等）<span style="color:red;">不可拥挤</span>；前往险峻处观光时应<span style="color:red;">充分考虑</span>自身的条件是否可行，不要强求和存侥幸心理。<br>
		3、游客登山或参与活动中<span style="color:red;">根据应自身身体状况</span>进行，注意适当休息，<span style="color:red;">避免过度激烈运动</span>以及自身身体无法适应的活动，同时做好防护工作。<br>
		4、在水上（包括江河、湖海、水库）浏览或活动时，<span style="color:red;">注意乘船安全要穿戴救生衣；不单独前往深水水域或危险河道</span>。<br>
		5、乘坐缆车或其他土载人观光运载工具时，应<span style="color:red;">服从景区工作人员安排</span>；遇超载、超员或其他异常时，<span style="color:red;">千万不要乘坐</span>，以防发生危险。<br>
		6、浏览期间游客应三两成群，不要独行。如果迷失方向，原则上应<span style="color:red;">原地等候导游</span>的到来或打电话求救、求助，千万不要着急。自由活动期间游客不要走的太远。带未成年人的游客，请认真履行监护责任，管好自己的孩子，<span style="color:red;">不能让未成年人单独行动</span>，并注意安全。<br>
		7、 在旅游行程中的自由活动时间，游客应当<span style="color:red;">选择自己能够控制风险的活动项目</span>，并在自己能够控制风险的范围内活动。除特殊团队外，旅行社不安排赛车、赛马、攀 岩、滑翔、探险性漂流、滑雪、下海、潜水、滑板、跳伞、热气球、蹦极、冲浪、跳伞等高风险活动，也敬请游客在旅游中的自由活动期间慎重考虑是否参与并自行 评估风险。
		</p>
	</div>
	<div class="termContent">
		<p>五、<span style="color:#73BF00;">购物娱乐</span>安全事项<br>
		1、<span style="color:red;">不要轻信</span>流动推销人员的商品推荐。无意购买时，不要向商家问价或还价。<br>
		2、要细心鉴别商品真伪，<span style="color:red;">不要急于付款购物</span>。购物时应向商家索取<span style="color:red;">正式发票</span>。<br>
		3、不要随商品推销人员到<span style="color:red;">偏僻地方</span>购物或取物。在热闹拥挤的场所购物或娱乐时，<span style="color:red;">注意保管好自己的钱包、提包、贵重的物品及证件</span>；不要单独行动，不要前往管理混乱的娱乐场所。游客在购物、娱乐时、主要应防止诈骗、盗窃和抢劫事故的发生。<br>
		4、在景点内娱乐时，应根据自身的条件参与适应的项目；在<span style="color:red;">自由活动期间</span>外出娱乐活动不要单独行动，不要前往管理混乱的娱乐场所，不要参与涉嫌违法的娱乐活动。<br>
		</p></div>
	<div class="termContent">
		<p>六、<span style="color:#73BF00;">其他</span>安全注意事项<br>
		1、注意听从导游的安排，<span style="color:red;">记住集中的时间和地点</span>；认清自己所乘坐的车型、车牌号及颜色；不要迟到，因迟到造成的后果由个人负责。<br>
		2、在旅游活动中，为了防止火灾事故的发生，<span style="color:red;">请不要携带易燃、易爆物品；不要乱扔烟头和火种</span> 。<br>
		3、 游客乘坐飞机旅游时，应带好自己的<span style="color:red;">有效身份证</span>（小孩应带户口本原件，满16岁未办理身份证的需持派出所带照片户籍证明原件），并注意保存好。导游发给你机 票时应先行<span style="color:red;">核对</span>自己机票上的姓名、往返时间、抵离目的地、航班号是否正确；请注意保存好机票和火车票并配合导游把票收回。如因游客自身原因造成不能登机， 旅行社概不负责。<br>
		4、游客在旅途中发生人身或财产意外事故时，按有关机构（如交通运输部门、酒店、保险公司、风景区管理单位）订立的条例或合同规定处理之根据。<br>
		5、所有行程本公司以其丰富之经验精心设计，本公司将竭尽所能以求行程之安全及住宿之旅馆按照行程所列来进行，但如因本公司<span style="color:red;">无法控制之因素</span>而必须改变行程更换旅馆，本公司当竭尽所能安排，但不须为任何变动复制。<br>
		6、为适应<span style="color:red;">各地交通时间及政治因素</span>之可能变动，并为维护团体之最高利益，本公司之领队有权视当时之情况保留所有行程之变动权及更换同等级旅馆之权利，团员不得异议。<br>
		7、有关之旅行安全问题，各航空公司班机取消或误点、游轮公司、汽车公司或各大饭店原有其个别之条例，直接对旅客负责，如遇交通延误，行李遗失或损坏，意外等事情，当根据各公司所定条例为解决之根据与本公司无涉。<br>
		8、旅途中所订之餐馆及游览等，均为本公司依丰富之经验所订，不得因个人之特殊要求更改，如欲变其所增加之费用，则由各自负责。<br>
		9、为维护团体之最高利益，诺是任何旅客有<span style="color:red;">妨碍团体正常活动</span>及利益的情况发生，本公司之领队人员有权取消之继续参加旅程之资格，自该旅客离团之时起，一切行动均与本公司无涉。<br>
		10、如遇罢工，政治因素，天气不良或其他人力不可抗拒之因素而导致行程延误而引起之额外费用，则由全体团员分担。
