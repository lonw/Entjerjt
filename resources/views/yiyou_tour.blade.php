		<p style="font-size: 4.6vh">世界医学技术与人类医学文明贡献：</p></br>
		​
		五获诺贝尔“生理学或医学奖”：</br>
		1923弗雷德里克·格兰特·班廷（Frederick Grant Banting）、约翰·詹姆士·理察·麦克劳德（John James Richard Macleod），发现胰岛素。</br>
		1966年，哈金斯（Charles B. Huggins），首创激素治癌。</br>
		1981年，大卫·休伯尔（David H. Hubel），发现脑部视觉系统的信息处理。</br> 
		2009年，杰克·绍斯塔克（Jack W. Szostak ），发现端粒和端粒酶如何保护染色体。</br>
		2011年，拉尔夫·斯坦曼（Ralph Steinman），发现树突状细胞和其在后天免疫中的作用。</br>
		        &emsp;&emsp;诺贝尔“生理学或医学奖”是对医学发明最高的肯定，从中我们可以看出加拿大的一直在为世界医学技术与人类医学文明做着不可磨灭的贡献。此外加拿大在心血管、癌症基因、神经学、急救系统、医疗设备、医疗器械等方面均为世界最顶级的，无论技术与设备均是世界各国学习或引进的目标。</br>
		       &emsp;&emsp;同时在百年内儿童医学的进步也是尤为显著，其中发现或解决了包括抗艾滋病药物拉米夫定，胰岛素溶血性尿毒综合症，马斯塔德手术，抗Rhy球蛋白等在内的多种医学难题。</br></br></br>
		 
		​
		<p style="font-size: 4.6vh">儿科（Pediatrics）：</p></br>
		​
		        &emsp;&emsp;加拿大的儿科是世界首屈一指的，加拿大安大略省也是北美最大的生物医药研究中心之一，曾成功生产了全球最早的儿童脑膜炎疫苗。而多家儿童医院内更以位于安大略省多伦多的加拿大儿童医院执掌牛耳。超过140年的历史使其的多个医学项目位列世界最前列。</br>
		        &emsp;&emsp;1、心脏中心：心脏中心成立于1940年，现已发展成全球最大，设备最先进、最齐全的心脏科。基于临床创新与医学创新的基础上提供最高质量的治疗。</br>
		        &emsp;&emsp;2、兔唇整形与修复中心：北美最大的兔唇整形与修复中心。拥有8名世界最顶尖的儿童兔唇整形与修复的全职专家坐诊。每年有超过120名新生儿得到治疗，并有超过3500名患兔唇儿童进行追踪治疗。</br>
		        &emsp;&emsp;3、移植耳蜗（人工耳蜗）中心：北美最大的耳蜗移植中心之一，从1990年起，移植了近1100名儿童，其中近500名为双耳移植。</br>
		        &emsp;&emsp;4、风湿病中心：儿童风湿中心成立于1983年。拥有北美最好的治疗团队之一，同时也是世界最顶尖的儿童风湿中心之一。</br>
		        &emsp;&emsp;5、癌症基因中心：包括白血病在内，不仅拥有多名遗传学、肿瘤学专家，并拥有极具人性化的康复团队，包括音乐治疗师、欢笑治疗师、物理治疗师、神经心理学专家、儿童生活专家、临床护理专家、老师（帮助孩子补习错过的课程）等。</br>
		        &emsp;&emsp;6、神经学中心：有超过10名世界超一流的医学博士研究关于小儿麻痹症与自闭症。拥有世界最先进的神经学治疗手法与设备仪器。</br>
		        &emsp;&emsp;7、骨骼健康中心：有着世界丰富的儿童佝偻病与骨质疏松的治疗经验，通过监测儿童体内钙、维生素D、磷酸盐代谢和甲状腺分泌来个性化治疗儿童骨骼畸形。</br></br></br>
