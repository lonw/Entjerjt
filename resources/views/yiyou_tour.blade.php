		<p style="font-size: 4.6vh">智慧医疗（Smart medical treatment）：</p></br>
		​
		 通过打造健康档案区域医疗信息平台，利用最先进的物联网技术，实现患者与医务人员、医疗机构、医疗设备之间的互动。</br>
		         &emsp;&emsp;加拿大突出项目：</br>
		         &emsp;&emsp;CISCO smart healthcare 项目；Toronto innovation Centre（主要关注智慧医疗项目的创新）；Startford, Ontario远程手术协助项目；Surrey 患者终端监控项目</br>
		         &emsp;&emsp;滑铁卢的Grand River Hospital是北美第一家拥有全面患者门户网站的医院。患者通过访问网站，可以让医师全面了解患者的诊断历史和疾病情况，实时更新他们的病症和药物副作用的影响，从而达到每个患者都得到最具体化、个性化的医疗保健。</br>
		&emsp;&emsp;Stratford在全程高速纤维网络覆盖的基础上，使当地医院可以远程进行高难度的手速。医疗专家只要通过在电脑前的指挥分歧，就能够完成许多高难度的医疗手术。</br>
		         &emsp;&emsp;加拿大与通信技术公司CISCO公司合作，立志于进一步提高患者的数据库管理，让医患之间能够更有效的进行交流。类似于Stratford的远程手术协助，但是加拿大要让其适用范围扩大到整个healthcare enterprise，从而大大减少实际医疗人员所需数量；以及患者病症图像的分析时间。</br>
