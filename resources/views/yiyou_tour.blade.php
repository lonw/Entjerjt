		<p style="font-size: 4.6vh">血脑障壁 （Blood-Brain-Barrier）：</p></br>
		​
		血脑障壁位于脑内并为半穿透，它允许某些物质穿透，但阻止某些物质通过。
		        血脑障壁有几项重要的功能：</br>
		        &emsp;&emsp;1. 保护脑部不受血液中外来物质的伤害</br>
		        &emsp;&emsp;2. 保护脑部远离来自身体其它部位之荷尔蒙与神经传导物质之影响</br>
		        &emsp;&emsp;3 .维持脑中环境的衡定</br>
		        &emsp;&emsp;加拿大著名医院Sunnybrook，创立于1943年，综合治疗成功率高达98.72%。同时也是世界第一家采用超声波化疗技术将药物植入脑内，做到脑内化疗且没有后遗症，同时治疗肿瘤并保护血脑障壁的医院。</br></br></br>
		​
		​
		<p style="font-size: 4.6vh">儿童心理疾病（Mental health）：</p></br>
		​
		        &emsp;&emsp;现如今越来越多的儿童患有心理疾病，包括自闭症、焦虑症等。在国内这方面病情是比较复杂的，成功率也不高。
		在加拿大North York General Hospital的IODE的儿童中心，常年致力于儿童心理、神经方面的疾病，且取得了显著的成果。同时North York General Hospital是拥有加拿大最完善的急诊室之一的医院，全年有超过98000名急诊病人，在综合治疗成功率上达到了99.45%。</br></br></br>
		​
		​
		<p style="font-size: 4.6vh">癌症（Cancer）:</p></br>
		​
		        &emsp;&emsp;其实对于癌症这个字眼，大家不要过于恐惧，除去部分癌症，大部分癌症都是可以得到治疗或者治愈的，就比方说像白血病、乳腺癌、肺癌等。</br>
		        &emsp;&emsp;加拿大在癌症治疗与研究方面常年占据世界最前列的位置。其中Princess Margaret Hospital:，这家1952年成立医院是加拿大最大的癌症中心，世界第五大的癌症中心，全医院综合治疗成功率高达99.27%。另外Sunnybrook reserch institute在研究癌症方面也是首屈一指，全院坐拥8名加拿大癌症研究所主席，近年来在预防癌症与癌症治疗方面也取得了极大的进展。</br></br></br>
		​
		<p style="font-size: 4.6vh">青光眼（Glaucoma）：</p></br>
		​
		        &emsp;&emsp;青光眼是导致人类失明的三大致盲眼病之一。如不及时治疗，视野可以全部丧失而至失明。2015年有一名患者同时患有青光眼和白内障，经过St Micheal’s hospital的专家手术治疗后，生活有了极大的改善。</br>
		        &emsp;&emsp;加拿大在白内障手术治疗上也是非常有名，拥有极高的手术成功率而且降低术后复发的可能性。不止是St Micheal’s，加拿大的眼科在全世界都是享誉盛名的。其中像McGill University Department of Ophthalmology，The Montreal Eye Institute ophthalmologists program等都是全世界最顶尖的。</br></br></br>
		​
		​
		 
		<p style="font-size: 4.6vh">糖尿病（Diabetes）：</p></br>
		​
		        &emsp;&emsp;糖尿病患病时长期存在的高血糖，导致各种组织，特别是眼、肾、心脏、血管、神经的慢性损害、功能障碍。
		成立于1968年的加拿大多伦多North York General Hospital 在糖尿病方面很有话语权，30年内每年有超过11000名糖尿病患者接受治疗且成果颇丰。</br>
		        &emsp;&emsp;目前市面上治疗糖尿病的常用药中，均含有胰岛素。而作为胰岛素的发现国，加拿大无论是在胰岛素的生产和研究领域都有着别国无可比拟的优势。 </br></br></br>
		 
		​
		<p style="font-size: 4.6vh">牙医（Dental）:</p></br>
		​
		        &emsp;&emsp;加拿大的牙科是世界最顶尖的国家之一，尤其是在牙科植体与材料更是全世界NO.1的存在。加拿大的牙科不像国内牙科那样繁琐复杂，且治疗技术更是远高于国内平均水平。另外加拿大在麻醉、止痛方面也是世界最一流的。</br>
		         &emsp;&emsp;Waterview Dental、Parkview Dental与The Richmond Dental Centre是多伦多排名前三的牙科诊所，在患者治疗后的反馈中我们也能发现这三家牙科诊所的好评度与专业度令人信服。</br></br></br>
		​
		​
		<p style="font-size: 4.6vh">医疗设备（Armarium）：</p></br>
		​
		        &emsp;&emsp;所有北美的新药新技术，首先会从加拿大开始实行；加拿大医院的硬件设施，比如仪器设备、病房病床、急救呼叫、医疗备案、医疗类别、药品制剂、医疗规章等均为世界首屈一指。</br>
		        &emsp;&emsp;同样加拿大医疗器械工业在国际上具有公认实力：心血管(如心脏瓣膜、电子心动描记仪)；放射疗法设备；医疗成像(如成像存档系统、脑磁描记仪系统)；试管诊断(如癌症、肝炎)；牙科植体及材料；辅助器械/家庭护理及移动辅助器械等。</br>
		         &emsp;&emsp;加拿大医疗器械大部分直接销售到医院、长期护理机构或美国和加拿大的经销商。由于加拿大优越的企业环境和研发环境，造就了一批业内著名企业，既有专注于发展的国际跨国公司，也有致力于研究成果的独立研究所。</br>
		         &emsp;&emsp;加拿大著名企业Bionik正在努力开发ARKE机器人是帮助下肢残障人民发明助行器。其公司的InMotion Series已经卖到了包含中，英，美等20多个国家，该系列产品包括了手、手腕以及手臂。这些复健仪器能够帮助中风和大脑性瘫痪患者有效的恢复行动，在业界内知名度甚高。</br></br></br>
