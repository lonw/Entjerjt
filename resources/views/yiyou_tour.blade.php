			&emsp;&emsp;随着医疗水平的日益完善，人们的生活水平也是与日俱进，但是依旧存在着不少在世界大部分国家得不到最合适的治疗技术与方法的疑难杂症，其中也包括中国。</br>
        &emsp;&emsp;现在随着出国旅游大趋势的阪上走丸，出国就医再也不成为困扰国民的一大难题了。</br>
        &emsp;&emsp;加拿大，西方工业七国之一，世界最发达的国家之一。其中加拿大的医学技术与研究水平更是世界超一流的水准，加拿大拥有世界最大的医学及生命科学科研集群，其下属的数十间研究机构产生的科研成果无论在数量或质量上都处在世界前十位。这也表明了很多在中国无法治愈的疾病，在加拿大就有可能可以得到医治。</br></br></br>
