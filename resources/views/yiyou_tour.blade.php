		<p style="font-size: 4.6vh">急救系统（Emergency system）：</p></br>
		​
		        &emsp;&emsp;基于完善的数据库的前提下，加拿大的病患在前往医院的途中，医院就已将病患血型、病史等数据提取出来并准备治疗所需事宜。甚至在病患前往医院的途中，就可以远程连接专家组进行初步诊断与初步治疗。</br>
		        &emsp;&emsp;在加拿大的急救体系中，由小型SUV急救车使用应急车道先行抵达病患处并提供基本的急救措施。随后大型急救车抵达现场，大型急救车内设齐全，同时医院内的数据库同步连接，保证病患在前往医院途中可以先得到基础治疗。医院内同步分析病患数据并准备治疗方案保证患者抵达医院的第一时间可以得到最合适的治疗。</br>
		        &emsp;&emsp;除却360°手术推车、大型医疗电梯等由加拿大发明并首推的设备外，加拿大在医学图像处理上更是全球最顶级的，譬如心电监护除颤仪、血压监测仪、快速心肺五项检验设备等。</br></br></br>
