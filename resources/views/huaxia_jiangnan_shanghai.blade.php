             1848年，西人在今南京东路、河南中路交汇处西北面建造了远东第一个跑马场。1850年又组织跑马总会，并举行比赛。<br>&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;
             1854年和平饭店落户外滩，是西侨在上海建造的第一家带餐饮业的旅社。1906年3月8日，和平饭店南楼汇中饭店重建，至此开始中国的酒店开始拥有电梯，自来水，电话，电灯，放映电影等设备。<br>&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;
            1914年起“先施”、“永安”、“大新”、“新新”这四大百货公司相继落户于上海大马路（现南京东路），继而成为开启中国现代百货业的钥匙。<br>&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;
     新中国成立后，揭开了上海发展的新篇章。上海不断扩大开放，深化改革，率先走出一条具有特大城市特点的科学发展之路。高楼大厦也如同雨后春笋一般拔地而起，如今陆家嘴中央商务区坐拥中国三大高度，其中包括总高632米118层的中国第一高度世界第二高度的上海中心。<br>&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;
