    上海开埠之后，外国商人等纷至沓来，为了满足南来北往客商住宿的需要，在洋泾浜(今延安东路)畔陆续开设了一些传统的客栈。1846年有一个名叫阿斯脱豪夫·礼查的英国人，在公馆马路(今金陵东路)外滩附近建起了一座近代化的旅馆，名为Astor House Hotel,它就是礼查饭店的前身。1860年，一幢两层楼的西式旅馆便出现在世人面前。它就是礼查饭店(现浦江饭店)。这是中国第一家西商饭店，专门接待外国旅客。解放后，中国的第一个证券交易所也坐落于此。<br>&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;
       1846年春，英国驻沪领事馆在如今的外滩落户，接着美国、法国、荷兰、奥地利、西班牙、德国、丹麦、意大利等国也陆续在附近设领事馆，从而形成了如今的十里洋场，万国建筑群。<br>&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;
