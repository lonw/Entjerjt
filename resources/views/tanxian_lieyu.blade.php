<script type="text/javascript">

    $(function(){

      $('.fanhuibtn').hover(function(){
        $('.triangle-left').css('border-right','40px solid rgba(188, 182, 181,1)');
        $('.returnBtn').css('background-color','rgba(188, 182, 181,1)');
      }, function(){
        $('.triangle-left').css('border-right','40px solid rgba(48, 197, 227,1)');
        $('.returnBtn').css('background-color','rgba(48, 197, 227,1)');
        });
        var scroller = $('.scroller div.innerScrollArea');
        var preBtn = $('.sprev');
        var nextBtn = $('.snext');
        var imgNow = $('.innerScrollArea ul li img')
        var scrollerContent = scroller.children('ul');
        var leftclicked=false;
        var rightclicked=false;
        scrollerContent.children().clone().appendTo(scrollerContent);
        var curX = 0;
        scrollerContent.children().each(function(){
            var $this = $(this);
            $this.css('left', curX);
            curX += $this.outerWidth(true);
        });
        var fullW = curX / 2;
        var viewportW = scroller.width();

        // Scrolling speed management
        var controller = {curSpeed:0, fullSpeed:2};
        var $controller = $(controller);
        var tweenToNewSpeed = function(newSpeed, duration)
        {
            if (duration === undefined)
                duration = 600;
            $controller.stop(true).animate({curSpeed:newSpeed}, duration);
        };

        // Pause on hover
        imgNow.hover(function(){
            var $this = $(this);
            $this.css('opacity', 0.5);
        }, function(){
            var $this = $(this);
            $this.css('opacity', 1);
        });

        

        $(document).click(function(e){ 
            e = window.event || e; // 兼容IE7
            obj = $(e.srcElement || e.target);
            if ($(obj).is(".scroller,.scroller *")) { 
            } else {
                if(leftclicked || rightclicked){
                  leftclicked=false;
                  rightclicked=false;
                  tweenToNewSpeed(0);
                }
            } 
        });
        preBtn.click(function(){
            if(rightclicked){
          rightclicked=false;
          tweenToNewSpeed(0);
        }
            if(!leftclicked){
              tweenToNewSpeed(controller.fullSpeed);
              leftclicked=true;
          }else{
            tweenToNewSpeed(0);
            leftclicked=false;
          }
        });
        nextBtn.click(function(){
          if(leftclicked){
            leftclicked=false;
            tweenToNewSpeed(0);
          }
          if(!rightclicked){
              tweenToNewSpeed(-controller.fullSpeed);
              rightclicked=true;
            }else{
              tweenToNewSpeed(0);
          rightclicked=false;
            }
        });
        // Scrolling management; start the automatical scrolling
        
        var doScroll = function()
        {
            var curX = scroller.scrollLeft();
            var newX = curX + controller.curSpeed;
            if (newX > fullW*2 - viewportW)
                newX -= fullW;
            scroller.scrollLeft(newX);
        };
        setInterval(doScroll, 20);
        //tweenToNewSpeed(controller.fullSpeed);


        $('a.fullsizable').fullsizable({
        detach_id: 'fullContainer'
      });

      $(document).on('fullsizable:opened', function(){
        $("#jquery-fullsizable").swipe({
          swipeLeft: function(){
            $(document).trigger('fullsizable:next')
          },
          swipeRight: function(){
            $(document).trigger('fullsizable:prev')
          },
          swipeUp: function(){
            $(document).trigger('fullsizable:close')
          }
        });
      });
    });
</script>
