        <div class="huaxia_content" style="width: 80%;margin: 0 auto;font-weight: bold;"> &emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;嵊泗列岛，位于杭州湾以东、长江口东南，是中国最东边的一个海岛县，全县有大小岛屿404个。陆域面积86平方公里，海域面积8738平方公里，是一个典型的海洋大县，陆域小县。嵊泗列岛海域水产资源丰富，有鱼类96种，虾类19种，蟹类15种，海藻类118种，还有软体动物及贝类。<br>&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;
     全国唯一：嵊泗列岛是全国唯一的国家级列岛风景名胜区，素有“海上仙山”的美誉，具有“碧海奇礁、金沙渔火”等原生态旅游特点，已被认定的风景点有50多处。嵊泗列岛风景名胜区划分为四个景区：即以碧海金沙、渔家休闲、海鲜美食为特色的泗礁景区，以远东第一大灯塔—花鸟灯塔和雾岛为特色的花鸟-绿华景区，以渔港、海崖和渔俗为特色的嵊山—枸杞景区，以幻石灵礁和现代港桥为特色的洋山景区。现今正在筹建第五景区-----东海五渔村景区。<br>&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;
     东海魚仓：嵊泗地处著名的舟山渔场中心，水产品资源丰富，被称为“海上牧场”。盛产带鱼、大小黄鱼、墨鱼、鳗鱼和蟹、虾、贝、藻等500多种海洋生物。嵊泗特产贻贝曾荣获国际农业博览会金奖首个“中国地理标志”集体商标产品，同时嵊泗列岛还被中国渔业协会授予“中国贻贝之乡”称号，拥有浙江省最大的贻贝产业化基地和深水网箱养殖基地，是宁波、上海及长三角地区鲜活水产品供应基地。<br>&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;
      海岛文化：嵊泗列岛不仅是一个美丽的旅游胜地，也是一个渔乡风情浓郁的岛城，它有文字记载的历史已有千余年。勤劳智慧的嵊泗人民，世世代代与海洋相生相伴，辛勤劳作于波谷浪峰，耕海牧渔，创造了瑰丽璀璨的特色海洋民俗文化，并且世代传承，使民族传统文化的底蕴得以延伸、延续，这块土地也并未因远离大陆而变得封闭贫瘠。<br>&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;
      祖国东极：嵊泗列岛作为祖国的东大门，连接公海，是国际航运的重要通道。从唐朝鉴真和尚东渡扶桑传经授道，到明朝抗倭战争、郑和下西洋、郑成功收复台湾，再到近代抗日战争，直至现代的前线海防，都发挥了至关重要的作用。枸杞岛上的“山海奇观”石刻，就出自明代都督侯继高之手，遒劲的笔锋刻录着嵊泗岛上千百年的风雨苍茫。解放后，刘少奇、彭德怀等国家领导人视察嵊泗时，对嵊泗海防给予了高度评价，称之为“东海前哨”。这些海防文化会给军事爱好者和其他游客以意外的收获。<br>&emsp;&emsp;&emsp;&emsp;&emsp;&emsp;
